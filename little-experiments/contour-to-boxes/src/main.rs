fn get_non_zero_points(vals: &[f32], width: usize) -> Vec<PointF> {
    let mut non_zero_points = Vec::new();

    for (i, &v) in vals.iter().enumerate() {
        if v != 0.0 {
            let y = (i as f32 / width as f32).floor();
            let x = i as f32 % width as f32;
            non_zero_points.push(PointF::from_yx(y, x));
        }
    }

    non_zero_points
}


